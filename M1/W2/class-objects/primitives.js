// String
const firstName = "Charlotte";
console.log(typeof firstName);

// Boolean
const isSunny = true;
console.log(typeof isSunny);

// Number
const speed = 78;
console.log(typeof speed);

const decimal = 9 / 40;
console.log(typeof decimal);

// Null
const pass = null;
console.log(typeof pass);

// Undefined
let experience;
console.log(typeof experience);

// Symbol
const sym = Symbol("hello");

// BigInt
const bigint = 98765432n;

// Primitive Data Type: Cannot be altered directly.
let greeting = "Hello World!";
console.log(greeting);
greeting.replace("Hello", "Hola");
console.log(greeting);
greeting = "Hey There!";
console.log(greeting);
